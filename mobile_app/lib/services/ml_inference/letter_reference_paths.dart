  /// Returns all strokes for a letter as a flat list of normalized Offsets.
  static List<Offset> getPath(String character) {
    final strokes = _raw[character.toUpperCase()];
    if (strokes == null) return [];
    final points = <Offset>[];
    for (final stroke in strokes) {
      for (final pt in stroke) {
        points.add(Offset(pt[0], pt[1]));
      }
    }
    return points;
  }
